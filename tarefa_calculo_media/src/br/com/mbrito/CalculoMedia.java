package br.com.mbrito;

public class CalculoMedia {

    public static void main(String[] args) {
        double nota1 = 10;
        double nota2 = 5.4;
        double nota3 = 4.6;
        double nota4 = 8.4;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println("A média é: " + media);
    }
}
